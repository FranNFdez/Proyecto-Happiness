/*2*/
CREATE TABLE PROFESORES (
    DNIprof CHAR(10) PRIMARY KEY,
    nombreProf VARCHAR(40),
    apeProf VARCHAR(60),
    despachoProf CHAR(8)
);

CREATE TABLE RECURSOS (
    recurso CHAR(4) PRIMARY KEY,
    descripRec VARCHAR(20) DEFAULT 'Impresora'
);

CREATE TABLE UTILIZACIONES (
    DNIprof CHAR(10),
    recurso CHAR(4),
    fechaInicio DATE,
    fechaFin DATE,
    PRIMARY KEY (DNIprof, recurso, fechaInicio),
    FOREIGN KEY (DNIprof) REFERENCES PROFESORES(DNIprof),
    FOREIGN KEY (recurso) REFERENCES RECURSOS(recurso)
);

